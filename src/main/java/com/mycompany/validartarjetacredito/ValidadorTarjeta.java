    private void botonComprobarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonComprobarActionPerformed
        ccv=Integer.parseInt(this.campoCcv.getText());
        this.campoCcv.setText("");
        numeroTarjeta=this.campoNumeroTarjeta.getText();
        mes= Integer.parseInt(this.campoMes.getText());
        this.campoMes.setText("");
        ano = Integer.parseInt(this.campoYear.getText());
        this.campoYear.setText("");
        try {
            if(validCcv(ccv) && ccv == (int)ccv) {
                if(mes > 0 && mes < 13 && ano > 2000) {
                    if(validar(numeroTarjeta)){
                        this.campoNumeroTarjeta.setText("El numero es v치lido");
                    }
                    else {
                        this.campoNumeroTarjeta.setText("El numero es inv치lido");
                    }
                }
                else {
                    this.campoNumeroTarjeta.setText("La fecha no es v치lida");
                }               
            }
            else {
                this.campoNumeroTarjeta.setText("El ccv es inv치lido");
            }
        }
        catch(NumberFormatException e) {
            System.out.println("El ccv es invalido");
        }
        
    }//GEN-LAST:event_botonComprobarActionPerformed
    public boolean validar(String tarjeta){
        return validator.isValid(tarjeta);
    }
    public boolean validCcv(int ccvNum) {
        boolean valido = false;
        if(String.valueOf(ccvNum).length() > 2 && String.valueOf(ccvNum).length() < 5) {
            valido = true;
        }
        return valido;
    }
